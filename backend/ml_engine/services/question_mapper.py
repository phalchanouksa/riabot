def get_question_text(question_index: int) -> str:
    """
    Convert question index (0-255) to human-readable Khmer text.
    
    NEA Official Scales:
    - ch1 (Interests, indices 0-95): Scale 1-4
    - ch2 (Skills, indices 96-255): Scale 0-3
    
    Args:
        question_index: Index from 0 to 255
        
    Returns:
        Khmer question text with rating instruction
    """
    if not 0 <= question_index < 256:
        return "សំណួរមិនត្រឹមត្រូវ"
    
    # Determine if it's ch1 (interest) or ch2 (skill)
    if question_index < 96:
        # ch1 - Interest questions (Scale 1-4)
        category = question_index // 6
        question_num = question_index % 6
        question_type = "ចំណាប់អារម្មណ៍"
        question_text = INTEREST_QUESTIONS[category][question_num]
        scale_text = "(1=អត់ចូលចិត្ត, 2=ចូលចិត្តតិចៗ, 3=ចូលចិត្តល្មម, 4=ចូលចិត្តខ្លាំង)"
    else:
        # ch2 - Skill questions (Scale 0-3)
        adjusted_index = question_index - 96
        category = adjusted_index // 10
        question_num = adjusted_index % 10
        question_type = "ជំនាញ"
        question_text = SKILL_QUESTIONS[category][question_num]
        scale_text = "(0=គ្មានជំនាញ, 1=ជំនាញតិចៗ, 2=ជំនាញបង្គួរ, 3=ជំនាញខ្លាំង)"
    
    major_name = MAJOR_NAMES_KH[category]
    
    # Format the question with appropriate scale
    return f"[{question_type}] {question_text}\n{scale_text}"


def get_question_info(question_index: int) -> dict:
    """
    Get detailed information about a question.
    
    Args:
        question_index: Index from 0 to 255
        
    Returns:
        Dict with question details
    """
    if not 0 <= question_index < 256:
        return {"error": "Invalid question index"}
    
    if question_index < 96:
        # ch1 - Interest
        category = question_index // 6
        question_num = question_index % 6
        question_type = "interest"
        question_text = INTEREST_QUESTIONS[category][question_num]
    else:
        # ch2 - Skill
        adjusted_index = question_index - 96
        category = adjusted_index // 10
        question_num = adjusted_index % 10
        question_type = "skill"
        question_text = SKILL_QUESTIONS[category][question_num]
    
    return {
        "index": question_index,
        "category": category,
        "category_name": MAJOR_NAMES_KH[category],
        "type": question_type,
        "question_number": question_num,
        "text": question_text,
        "full_question": get_question_text(question_index)
    }


# For Rasa integration - export simple function
def index_to_question(index: int) -> str:
    """Simple wrapper for Rasa - converts index to question text."""
    return get_question_text(index)
